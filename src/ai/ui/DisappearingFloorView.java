package ai.ui;

public class DisappearingFloorView {
    public final int x;
    public final int y;
    public final int width;
    public final int height;
    public final String state;

    public DisappearingFloorView(int x, int y, int width, int height, String state) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.state = state;
    }
}
